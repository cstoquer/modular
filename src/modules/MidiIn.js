var Module  = require('../core/Module');
var MIDI    = require('../core/MIDI');

//▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄
function MidiIn() {
	Module.call(this);
	MIDI.open();
	var t = this;

	this.onMessage = function (event) {
		t.$OUT.emit(event);
	};

	MIDI.on('message', this.onMessage);
}
inherits(MidiIn, Module);

//▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄
MidiIn.prototype.remove = function () {
	MIDI.removeListener('message', this.onMessage);
	Module.prototype.remove.call(this);
};

//▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄
MidiIn.prototype.descriptor = {
	type: 'MidiIn',
	name: 'MidiIn',
	size: 1,
	inputs:  {},
	outputs: { OUT: { type: 'event', x: 5,  y: 0 } },
	controls: {}
};

module.exports = MidiIn;